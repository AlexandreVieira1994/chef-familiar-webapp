  update recipe_ingredients ri
  set
    quantity = v.quantity,
    unit = v.unit,
    category = v.category,
    image_url = v.image_url
  from recipes r
  join ingredient_seed v on r.code = v.recipe_code
  where ri.recipe_id = r.id
    and ri.ingredient_name = v.ingredient_name
  returning ri.id
)
insert into recipe_ingredients (recipe_id, ingredient_name, quantity, unit, category, image_url)
select r.id, v.ingredient_name, v.quantity, v.unit, v.category, v.image_url
from recipes r
join ingredient_seed v on r.code = v.recipe_code
where not exists (
  select 1
  from recipe_ingredients ri
  where ri.recipe_id = r.id
    and ri.ingredient_name = v.ingredient_name
);
